package Codeforces;

import java.util.Arrays;
import java.util.Scanner;

public class m2019b {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-- > 0) {
            int n = sc.nextInt(), q = sc.nextInt();
            int[] arr = new int[10];
            int[] num = new int[n];
            for (int i = 0; i < n; i++) {
                num[i] = sc.nextInt();
            }
            for (int i = 0; i < n; i++) {
                for (int j=i+1;j<n;j++) {
                    arr[num[j] - 1]++;
                    arr[num[i] - 1]++;
                }
            }
            System.out.println(Arrays.toString(arr));
        }
    }
}
