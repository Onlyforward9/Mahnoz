package Codeforces;

import java.util.Scanner;

public class m1926e {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-- > 0) {
            int n = sc.nextInt();
            int k = sc.nextInt();
            int[] tok = new int[k];
            int index = 0;
            for (int i = 1; i <= n; i += 2) {
                tok[index] = i;
                index++;
            }

        }
    }
}
